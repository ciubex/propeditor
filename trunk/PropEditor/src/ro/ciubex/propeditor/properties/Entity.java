package ro.ciubex.propeditor.properties;

/**
 * A replacement of java property entity
 * 
 * @author Claudiu Ciobotariu
 * 
 */
public class Entity implements Cloneable {
	private Type type;
	private String key;
	private String content;

	public Entity(Type type, String content) {
		this(type, null, content);
	}

	public Entity(String key, String content) {
		this(Type.PROPERTY, key, content);
	}

	public Entity(Type type, String key, String content) {
		this.type = type;
		this.key = key;
		this.content = content;
	}

	public Type getType() {
		return type;
	}

	public void setType(Type type) {
		this.type = type;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((type == null) ? 0 : type.hashCode());
		result = prime * result + ((key == null) ? 0 : key.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Entity other = (Entity) obj;
		if (type != other.type)
			return false;
		if (key == null) {
			if (other.key != null)
				return false;
		} else if (!key.equals(other.key))
			return false;
		return true;
	}

	/**
	 * Clone an entity.
	 */
	@Override
	protected Object clone() {
		Entity en = new Entity(type, key, content);
		return en;
	}

}
